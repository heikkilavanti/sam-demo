def test_valid_event_returns_201(valid_apigw_event, mocker):
    mocker.patch.object(post.Db, 'create_task', return_value="af56f77a474a46c6ab1c5296e65924a7")
    response = post.handler(valid_apigw_event, "")
    data = json.loads(response["body"])

    assert response["statusCode"] == 201
    assert data["taskId"] == "af56f77a474a46c6ab1c5296e65924a7"

